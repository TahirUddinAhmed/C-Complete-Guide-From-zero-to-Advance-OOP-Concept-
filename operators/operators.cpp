#include <iostream>
using namespace std;

int main() {
    // operators in c++
    // arithmetic operators: +, -, *, /
    int a = 5, b = 2;

    cout<<"addition = " << a + b << endl; // 5+ 4
    cout<<"subtraction = " << a - b << endl; // 5 - 2
    cout<<"multiplication = " << a * b << endl; // 5 * 2
    cout<<"division = " << a / b << endl; // 5 / 2

    return 0;
}